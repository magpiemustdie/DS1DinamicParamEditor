    public class InvItem
    {
        public int Id { get; }
        public string Name { get; }
        public int StackLimit { get; }
        public int UpgradeType { get; }

        public InvItem(int id, int stackLimit, int upgradeType, string name)
        {
            Id = id; StackLimit = stackLimit; UpgradeType = upgradeType; Name = name;
        }
        public override string ToString() => Name;
    }

    public class InvCategory
    {
        public string Name { get; }
        public int Id { get; }
        public bool ShowIds { get; }
        public List<InvItem> Items { get; }

        public InvCategory(string name, int id, List<InvItem> items, bool showIds = false)
        {
            Name = name; Id = id; Items = items; ShowIds = showIds;
            Items.Sort((a, b) => a.Name.CompareTo(b.Name));
        }
        public override string ToString() => Name;
    }

